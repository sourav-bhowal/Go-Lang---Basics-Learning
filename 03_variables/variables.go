package main

import "fmt"

func main() {
	// Declare a variable named "x" of type int and assign it the value 10.
	var x int = 10

	// Print the value of "x" to the console.
	fmt.Println(x)

	// Declare a variable named "y" of type string and assign it the value "Hello, World!".
	var y string = "Hello, World!"

	var y2 = "Hello, World! 2" // The type of y2 is inferred to be string based on the assigned value.

	// Print the value of "y" to the console.
	fmt.Println(y)

	// 
	fmt.Println(y2)

	// Declare a variable named "z" of type bool and assign it the value true.
	var z bool = true

	// Print the value of "z" to the console.
	fmt.Println(z)

	// Shorthand 
	c:= "Sourav"

	fmt.Println(c)
}